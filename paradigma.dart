// Dengan OOP, kita bisa dengan mudah memvisualisasikan objek 
// yang ada di dunia nyata ke dalam program komputer. 
// Sebagai contoh, bayangkan kucing sebagai sebuah objek. 
// Objek kucing ini memiliki karakteristik seperti warna bulu, usia kucing, dan berat badan. 
// Ciri-ciri ini disebut dengan attributes atau properties. 

// Selain itu kucing juga bisa melakukan beberapa hal seperti makan, tidur, dan bermain. 
// Perilaku pada objek kucing ini adalah sebuah method.

// OOP merupakan paradigma yang berdasarkan pada konsep objek yang memiliki 
// atribut serta dapat melakukan operasi atau prosedur tertentu.

// Terdapat 4 (empat) pilar dalam pemrograman berorientasi objek, antara lain: 
// encapsulation, abstraction, inheritance, dan polymorphism.

// Terdapat 4 (empat) pilar dalam object oriented programming, antara lain:

// Encapsulation, kondisi di mana status atau kondisi di dalam class, dibungkus, dan bersifat privat. 
// Artinya objek lain tidak bisa mengakses property secara langsung.

// Abstraction, objek hanya menunjukkan operasinya secara high-level. 
// Cukup tau method apa saja yang dibuat oleh class tersebut.

// Inheritance, mewarisi properti kelas induk (parent class) ke kelas anak (child class). 

// Polymorphism, objek class dapat memiliki bentuk atau implementasi yang berbeda-beda pada satu metode yang sama.

// Class merupakan blueprint untuk mendefinisikan karakteristik dari sebuah objek.
// Di dalam class, variabel berlaku sebagai properti, sementara fungsi sebagai method.
// Abstract Class merupakan gambaran umum dari sebuah kelas. Abstract class tidak dapat direalisasikan ke dalam sebuah objek.
// Mixin digunakan untuk menggabungkan dan memanfaatkan property/method dari beberapa kelas lain.
// Extension methods digunakan untuk membuat method tambahan pada kelas yang sudah ada.