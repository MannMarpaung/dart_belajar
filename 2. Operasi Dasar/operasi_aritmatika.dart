void main() {
  // operasi aritmatika
  var a = 10;
  var b = 5;

  // penjumlahan
  print(a + b);
  // pengurangan
  print(a - b);
  // perkalian
  print(a * b);
  // pembagian
  print(a / b);
  // sisa bagi
  print(a % b);
  // increment
  print(a++);
  // decrement
  print(a--);
  // penjumlahan
  print(a += b);
  // pengurangan
  print(a -= b);
}