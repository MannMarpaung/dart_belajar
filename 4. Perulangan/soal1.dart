void main() {
  int a;

  for (a = 1; a <= 10; a++) {
    print("*" * a);
  }

  print("=================================================================================");

  int b = 10;

  while (b > 0) {
    print("*" * b);
    b--;
  }

  print("=================================================================================");

  int c = 10;

  do {
    print("*" * c);
    c--;
  } while (c > 0);
}